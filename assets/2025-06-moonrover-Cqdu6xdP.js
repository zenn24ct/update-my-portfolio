JavaScriptを使用して月面ローバーのゲームを作成しました。  
プレイヤーは月面を探索し、障害物を避けながら目的地に到達することを目指します。  

- 矢印キーでローバーを操作  
- 障害物にぶつかるとゲームオーバー  
- 赤茶色と茶色の石に触れるとスコア獲得  
- 石は毎回自動生成  

この月面ローバーゲームは、今後ミッション機能を追加予定です。  
深宇宙展にて、本物の月面ローバーを見て、あまりの大きさに衝撃を受けました。

